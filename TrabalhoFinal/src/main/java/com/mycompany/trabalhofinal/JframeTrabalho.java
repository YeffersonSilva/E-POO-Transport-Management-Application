    private void CadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastroActionPerformed
        TrabalhoFinal tf = new TrabalhoFinal();



        
        
        float n = Float.parseFloat(Distancia.getText());
        float n1 = Float.parseFloat(volume.getText());
        String nome1 = NombreReme.getText();
        String nome2 = NombreDestino.getText();

        tf.Cadastro(nome1, nome2, n1, n);

        Distancia.setText("");
        volume.setText("");
        NombreDestino.setText("");
        NombreReme.setText("");

        String Ob = tf.ObteCastro();
        //jTextArea2.setText(Ob);
        jTextArea2.setText("");

        jTextArea2.setText(Ob);
        try {
            tf.GrabarAchivo();
        } catch (IOException ex) {
            Logger.getLogger(JframeTrabalho.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_CadastroActionPerformed

    private void FecharCargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FecharCargaActionPerformed
        jButton1.setEnabled(true);
        TrabalhoFinal tf = new TrabalhoFinal();
        String sr = null;
        try {
            sr = tf.FecharCarga();
            
        } catch (IOException ex) {
            Logger.getLogger(JframeTrabalho.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            tf.GravaArchivoParaPila();

        } catch (IOException ex) {
            Logger.getLogger(JframeTrabalho.class.getName()).log(Level.SEVERE, null, ex);

        }
        try {
            tf.LeePila();
        } catch (IOException ex) {
            Logger.getLogger(JframeTrabalho.class.getName()).log(Level.SEVERE, null, ex);
        }
        String Ob = tf.ObteCastro();
        jTextArea2.setText(Ob);
        
        
    }//GEN-LAST:event_FecharCargaActionPerformed

